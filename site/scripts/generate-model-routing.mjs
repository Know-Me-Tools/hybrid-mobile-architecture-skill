import {readFile, writeFile} from 'node:fs/promises';
import path from 'node:path';
import process from 'node:process';
import {parse as parseYaml} from 'yaml';

const repoRoot = path.resolve(import.meta.dirname, '..', '..');
const registryPath = path.join(repoRoot, 'docs', 'prompting', 'model-registry.yaml');
const outputPath = path.join(repoRoot, 'docs', 'prompting', 'model-routing.generated.md');
const checkOnly = process.argv.includes('--check');

const roleLabels = new Map([
  ['role-frontier-architect', 'Frontier architecture and hard debugging'],
  ['role-balanced-producer', 'Balanced implementation producer'],
  ['role-mechanical-transformer', 'Mechanical transformation'],
  ['role-research-synthesizer', 'Research and synthesis'],
  ['role-independent-critic', 'Independent critic'],
  ['role-long-context-cartographer', 'Long-context cartography'],
  ['role-multimodal-reviewer', 'Multimodal review']
]);

const registry = parseYaml(await readFile(registryPath, 'utf8'));

function modelLabel(model) {
  return `${model.model_id} (${model.provider})`;
}

function routeable(model) {
  return model.confidence !== 'unsupported' && !String(model.availability).includes('unavailable');
}

function row(model) {
  const status = model.model_id_kind === 'family' ? 'family/route' : 'exact';
  const confidence = model.confidence;
  const constraints = model.known_constraints.join(', ');
  return `| \`${model.model_id}\` | ${model.provider} | ${status} | ${confidence} | ${model.availability} | ${constraints} |`;
}

const lines = [
  '---',
  'sidebar_position: 4',
  'title: Generated model routing',
  'slug: /model-routing',
  '---',
  '',
  '# Generated model routing',
  '',
  '<!-- generated by site/scripts/generate-model-routing.mjs; edit docs/prompting/model-registry.yaml instead -->',
  '',
  `**Registry verified at:** ${registry.verified_at}`,
  `**Freshness window:** ${registry.freshness_policy.max_age_days} days`,
  '',
  registry.disclaimer,
  '',
  '## Requested Label Accountability',
  '',
  '| Requested label | Status | Canonical value | Evidence gap |',
  '|---|---|---|---|',
  ...registry.requested_inventory.map((item) => {
    const canonical = item.canonical_model_id ?? item.canonical_family ?? 'none';
    return `| ${item.requested_label} | ${item.registry_status} | \`${canonical}\` | ${item.evidence_gap ?? ''} |`;
  }),
  '',
  '## Role Routing',
  ''
];

for (const [roleId, label] of roleLabels.entries()) {
  const matches = registry.models.filter((model) => routeable(model) && model.recommended_roles.includes(roleId));
  lines.push(`### ${label}`, '');
  if (!matches.length) {
    lines.push('No currently routeable model is assigned to this role.', '');
    continue;
  }
  lines.push('| Model | Provider | ID type | Confidence | Availability | Constraints |');
  lines.push('|---|---|---|---|---|---|');
  lines.push(...matches.map(row));
  lines.push('');
}

lines.push('## Critic Separation Rule', '');
lines.push('Use a different model family for independent criticism when one is available in the harness. A producer model cannot certify its own output; the critic must grade observable acceptance evidence against the original requirements.');
lines.push('');
lines.push('## Source Records', '');
lines.push('');
for (const model of registry.models) {
  lines.push(`### ${modelLabel(model)}`, '');
  lines.push(`- ID type: ${model.model_id_kind}`);
  lines.push(`- Confidence: ${model.confidence}`);
  lines.push(`- Sources: ${model.sources.map((source) => `[${source.id}](${source.url}) accessed ${source.accessed_at}`).join('; ')}`);
  lines.push('');
}

const generated = `${lines.join('\n')}\n`;

if (checkOnly) {
  let current = '';
  try {
    current = await readFile(outputPath, 'utf8');
  } catch {
    console.error(`missing generated routing file: ${path.relative(repoRoot, outputPath)}`);
    process.exit(1);
  }
  if (current !== generated) {
    console.error(`model routing drift: run npm --prefix site run generate:model-routing`);
    process.exit(1);
  }
  console.log('model routing has no drift');
} else {
  await writeFile(outputPath, generated);
  console.log(`wrote ${path.relative(repoRoot, outputPath)}`);
}
